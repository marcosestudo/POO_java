package poo._if_else;

public class App {

    public static void main(String[] args) {
        String letra = "b";

        if (letra == "a") {
            System.out.println(1);
        } else if (letra == "b") {
            System.out.println(2);
        } else {
            System.out.println(3);
        }

    }
}
